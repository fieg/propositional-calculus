namespace Fieg\PC\Proposition;

use Fieg\PC\Proposition;

class Atomic extends Proposition
{
    protected $name;

    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    public function __toString()
    {
        return (string) $this->name;
    }
}
